#include"cristian.h"

float suma (float a, float b)
{
    float result;
    result= a+b;
    return result;
}

float resta (float a, float b)
{
    float result;
    result= a-b;
    return result;
}

float multiplicacion (float a, float b)
{
    float result;
    result= a*b;
    return result;
}

float division (float a, float b)
{
    float result;
    result= a/b;
    return result;
}
