/**
\fn void SetPINSEL ( uint8_t , uint8_t , uint8_t )
\brief Activa el modo de trabajo de los puertos
\details Configura el PINSEL correspondiente al puerto seleccionado
\param [in] PUERTO: Número de puerto  PUERTO_0,PUERTO_1,PUERTO_2,PUERTO_3,PUERTO_4
\param [in] PIN   : Número de pin del puerto de 0 a 32
\param [in] modo: Función del pin del puerto  de 0 a 3
\return Retorna void
*/

 void SetPINSEL( uint8_t PUERTO, uint8_t PIN, uint8_t MODO )
{
	  PUERTO = ( 2 * PUERTO + PIN / 16 ) ;
	  PIN = ( PIN % 16 ) * 2;
	  PINSEL[ PUERTO ] &= ~( 0x03 << PIN);
	  PINSEL[ PUERTO ] |= ( MODO << PIN );
}
